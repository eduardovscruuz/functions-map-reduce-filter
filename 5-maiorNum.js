// Escreva um programa onde, você chame uma função mandando dois 
//  argumentos, 2 números, e a função responde qual número é maior

function maiorNum(num1, num2){
    if(num1>num2){
        console.log(`${num1} é o maior número!`)
    }
    else{
        console.log(`${num2} é o maior número!`)
    }
}


maiorNum(10,2)
maiorNum(14,22)
